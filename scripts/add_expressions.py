def add_expressions():
    """Add expressions to the pitch database."""
    if not DB_PATH.exists():
        print(f"Error: Database not found at {DB_PATH}")
        print("Run: python scripts/download_dictionary.py first")
        return

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    added = 0
    skipped = 0
    updated = 0

    for surface, reading, accent, goshu, goshu_jp in EXPRESSIONS:
        # Check if entry already exists
        cursor.execute(
            "SELECT id, accent_pattern FROM pitch_accents WHERE surface = ? AND reading = ?",
            (surface, reading)
        )
        existing = cursor.fetchone()

        if existing:
            existing_id, existing_accent = existing
            if existing_accent == accent:
                skipped += 1
            else:
                # Update if accent pattern is different
                cursor.execute(
                    """
                    UPDATE pitch_accents
                    SET accent_pattern = ?, goshu = ?, goshu_jp = ?, data_source = 'expressions'
                    WHERE id = ?
                    """,
                    (accent, goshu, goshu_jp, existing_id)
                )
                updated += 1
                print(f"  Updated: {surface} ({reading}) {existing_accent} -> {accent}")
        else:
            # Insert new entry
            cursor.execute(
                """
                INSERT INTO pitch_accents (surface, reading, accent_pattern, goshu, goshu_jp, data_source)
                VALUES (?, ?, ?, ?, ?, 'expressions')
                """,
                (surface, reading, accent, goshu, goshu_jp)
            )
            added += 1
            print(f"  Added: {surface} ({reading}) accent={accent}")

    conn.commit()
    conn.close()

    print(f"\nSummary:")
    print(f"  Added: {added}")
    print(f"  Updated: {updated}")
    print(f"  Skipped (already exists): {skipped}")
    print(f"  Total expressions in list: {len(EXPRESSIONS)}")


if __name__ == "__main__":
    print("Adding common expressions to pitch database...\n")
    add_expressions()
    print("\nDone!")
